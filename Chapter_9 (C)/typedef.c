#include<stdio.h>

int main(){
    typedef int Pratyush;
    Pratyush a= 88;

    printf("The value of Pratyush is %d\n" , a);
    return 0;
}


// (typedef) keyword is used to define your own datatype.
// Here, integer data-type is replaced with Pratyush, i.e., Pratyush can be used in this program to define a variable as int. 
// (int) can also be used as always.
